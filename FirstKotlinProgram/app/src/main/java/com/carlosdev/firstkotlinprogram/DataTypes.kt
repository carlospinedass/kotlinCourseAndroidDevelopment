fun main(args: Array<String>) {
    var a: Boolean = true // Range: true or false
    var b: Char = 'R'
    var c: Byte = 12 // Range: [-128, 127]
    var d: Short = -32768 // Range: [-32,768, 32,768]
    var e: Int = 43543 // Range: [-2^31, 2^31-1]
    var f: Long = -432543234L // Range: [-2^63, 2^63-1]
    var g: Float = 5.68764324F // Range: 32 bit floating point
    var h: Double = 7.34534276464 // Range: 64 bit foating point

    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
}